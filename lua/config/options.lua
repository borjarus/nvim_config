-- OPTIONS
local set = vim.opt

--line nums
set.relativenumber = true
set.number = true

set.splitbelow = true
set.splitright = true

set.wrap = false

-- indentation and tabs
set.tabstop = 2
set.shiftwidth = 2
set.autoindent = true
set.expandtab = true

-- search settings
set.ignorecase = true
set.smartcase = true

-- appearance
set.termguicolors = true
set.signcolumn = "yes"

-- cursor line
set.cursorline = true

-- 100th column
set.colorcolumn = "100"

-- keep cursor at least 8 rows from top/bot
set.scrolloff = 8

-- incremental search
set.incsearch = true

-- faster cursor hold
set.updatetime = 50

set.clipboard = "unnamedplus"

set.virtualedit = "block"
set.inccommand = "split"
set.ignorecase = true

